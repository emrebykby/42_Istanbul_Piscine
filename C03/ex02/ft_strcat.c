char	*ft_strcat(char *dest, char *src)
{
	int	c;
	int	d;

	d = 0;
	c = 0;
	while (dest[c] != '\0')
	{
		c++;
	}
	while (src[d] != '\0')
	{
		dest[c] = src[d];
		c++;
		d++;
	}
	dest[c] = '\0';
	return (dest);
}
